#include <iostream>
using namespace std;
int main(){
	cout << "=====Operator Aritmatika=====\n";
	int a, b;
	cout << "Input nilai A = ";
	cin >> a;
	cout << "Input nilai B = ";
	cin >> b;
	cout << "Hasil Jumlah = " << a + b <<
	endl;
	cout << "Hasil Kurang = " << a - b <<
	endl;
	cout << "Hasil Kali = " << a * b <<
	endl;
	cout << "Hasil Bagi = " << a / b <<
	endl;
	return 0;
}

